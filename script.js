    function playHoverAudio(item) {
        stopHoverAudio();
        try {
            const ctx = getAudioContext();
            const now = ctx.currentTime;
            const freqs = item.audioFreq || [440, 554, 659];
            activeAudio = {
                oscs: freqs.map((f, i) => {
                    const osc = ctx.createOscillator();
                    const gain = ctx.createGain();
                    osc.type = i === 0 ? 'sine' : 'triangle';
                    osc.frequency.setValueAtTime(f, now + i * 0.18);
                    gain.gain.setValueAtTime(0.15, now + i * 0.18);
                    gain.gain.exponentialRampToValueAtTime(0.001, now + i * 0.18 + 1.5);
                    osc.connect(gain);
                    gain.connect(ctx.destination);
                    osc.start(now + i * 0.18);
                    osc.stop(now + i * 0.18 + 1.6);
                    return { osc, gain };
                }),
                ctx
            };
        } catch (e) { /* 静默 */ }
    }
    function stopHoverAudio() {
        if (activeAudio && activeAudio.oscs) {
            try {
                const now = activeAudio.ctx.currentTime;
                activeAudio.oscs.forEach(({ gain }) => {
                    gain.gain.cancelScheduledValues(now);
                    gain.gain.setValueAtTime(0.001, now);
                });
            } catch (e) { /* 静默 */ }
            activeAudio = null;
        }
    }

